# Запрашиваем ввод числа у пользователя
x = int(input("Введите число: "))

# Проверяем случай 1: если число отрицательное
if x < 0:
    print("Ошибка: число не должно быть отрицательным!")
# Случай 2: если число положительное
elif x > 0:
    print(f"Первые {x} четных чисел:")
    
    # Используем цикл for для вывода первых x четных чисел
    for i in range(1, x + 1):
        print(2 * i, end=" ")  # Выводим четное число: 2, 4, 6 и т.д.

else:
    print("Число равно нулю, четных чисел выводить не нужно.")




   